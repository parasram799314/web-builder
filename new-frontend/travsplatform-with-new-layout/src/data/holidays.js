export function getMergedHolidays(selectedCountries) {
  const merged = {};
  selectedCountries.forEach((countryKey) => {
    const countryHolidays = ALL_HOLIDAYS[countryKey] || {};
    Object.entries(countryHolidays).forEach(([date, info]) => {
      if (merged[date]) {
        merged[date] = {
          ...merged[date],
          name: merged[date].name.includes(info.name)
            ? merged[date].name
            : `${merged[date].name} / ${info.name}`,
          countries: [...(merged[date].countries || [merged[date].country]), countryKey],
        };
      } else {
        merged[date] = { ...info, country: countryKey, countries: [countryKey] };
      }
    });
  });
  return merged;
}

